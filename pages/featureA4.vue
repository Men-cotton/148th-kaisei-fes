    <div class="ft-subtitle">二　名詞としての「恋」</div>
    <p class="ft-text">
      調べ始めてわかったが、これは随分と難しい問題のようだ。辞書ごとに様々な記述がなされているから、その内容を精査しながら進めねばならぬ。因みに今回使った岩波国語辞典・明鏡国語辞典・広辞苑の記述はそれぞれ【資料篇】としてまとめてあるから、ご参照いただきたい。
      <br>　それでは名詞としての「恋」について。
      <br><br>①「恋」は気持だけを表すのか、心の活動までをも表すのか
      <br>　岩波の「恋」の項の説明が興味深い。
      <br>
      <br>「▽本来は、（異性に限らず）その対象にどうしようもないほど引きつけられ、しかも、満たされず苦しくてつらい気持を言う。「―に焼けて死ぬ虫になったって、思いはとげて見せるぞ」のような用例から見て取れるとおり、心の活動（やその内容）を言う「思い」とは区別される。「―は楽し、野辺の花よ」のような言い方は、一九一〇年代ごろからのもの。」
      <br>
      <br>つまり、「恋」が「恋という動作」を表すに留まって、「恋心」までは表さないのだと言う。
