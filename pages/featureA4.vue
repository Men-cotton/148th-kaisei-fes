      <br><br>②「恋」に対象は存在するのか
      <br>　一からかなり分かってくるだろうか。「恋」がその動作自体を表す名詞ならば、「恋」に存在するのは対象というより「動作の原因」に近くなると思う。この話は四章で触れたい。
    </p>
    <div class="ft-subtitle">三　サ変動詞としての「恋」</div>
    <p class="ft-text">
      <br>①サ変動詞の成立
      <br>　まず、サ変動詞というものが何だったか振り返ろう。「サ変動詞」というのは、動詞「する」と同様の活用をする動詞のことだ。「せ（ず）／したい／する。／する（こと）／すれ（ば）／せよ。」などの形が主にある。そして、「する」の上に他の言葉がくっついてできる動詞も、同じくサ変動詞と呼ばれる。
      <br>「サ変動詞の構成<br>
      サ変動詞の多くは、漢語の名詞に「する」が付いた複合語である。 この他、外来語を語幹とするもの（例：「キャッチする」）、 和語の名詞＋「する」の形のもの（例：「早起きする」）、擬態語＋「する」の形のもの（例：「どきどきする」）が見られる。 「達する」「全うする」など、「する」の前の部分（語幹）が単独では単語として使われない形のものもある。」
      <br>（Wikipedia「サ行変格活用動詞」二〇一八年九月閲覧）
      <br>　さて、「恋する」もこのサ変動詞に含まれるわけだが、成り立ちから言うと、「恋」は訓読みをするので、右の引用部分では「和語の名詞＋する」に当たる。「愛する」が漢語由来の複合語なので、その点で「恋する」の方が若干俗っぽい（新しい？）表現かもしれない。現に、古語辞典に「恋す」が収容されていないことが多い（旺文社「全訳古語辞典　第三版」など）。
      <br>
      <br>②恋はするもの
      <br>　さて、「恋する」がくだけた言い方であるということを前提に考えれば、面白いことがわかってくる。
      <br>　まず単純に考えて、「恋をする」とは言っても、「愛をする」とは言わない。
      <br>　名詞「恋」が後ろに「する」を取れるのはなぜだろうか。恐らく「恋」は、例えば「読書」のように、『行為』を表す名詞なのだろうと考えられる。（少なくともそのようなニュアンスは含むに違いない）。つまり、「恋」というだけで「恋する」という『行動』がイメージされるのだ。
      <br>　一方の「愛」だが、これは「愛を告白する」のように用い、「愛」だけで「愛する」という動作は連想されない。あくまで「愛」とは感情、気持ちなのだ。
      <br>　先述の「恋する」がくだけた言い方である、という話と合わせて考えると、「恋する」は「恋をする」の略形として捉えられがちである、とも考えられる。
    </p>

    <div class="ft-subtitle">四　「に」と「を」</div>
    <p class="ft-text">
      <br>　ところで、「恋」の動詞形には「恋する」の他に「恋う」というものがある。この二つを比べてみよう。
      <br>　恋うというのは、広辞苑からも分かる通り本来「に」を受けて、「母に恋う」というふうに言った。それが平安以降「を」を受けるようになったという。これが何を意味するか。現在でも「恋する」が「に」を受け、「愛する」が「を」を受けることからすると、一つ言えるのは、平安期に「恋う」が「思う」の類の動詞に分類されたのではないか、ということだ。
      <br>　「思う」の類の動詞とは、特に専門用語などではない。ここでは「愛する」「慕う」「敬う」のように、その動詞だけで「愛おしく思う」「親しく思う」「尊く思う」と「思う」のニュアンスを含むものを指している。そしてそれらは思いの対象として「を」を受けるのだ。
      <br>　一方「恋する」の例では、その動詞だけでは「思う」のニュアンスを含まない。故に、対象を表す助詞「を」は取らずに、むしろ動作の原因「に」を取るのだ。
      <br>　しかし、「恋う」は違った。「故郷を恋う」と書けば、「故郷を恋しく思う」という風に訳せるのだ。これは「恋」のそもそもの用法から考えればおかしな話だが、「恋う」がサ変動詞、つまり《名詞＋する》という形を取らなかったがために、一つの動詞としての個性が強くなり、結果的に「恋」という名詞から（ニュアンスの面で）一定の距離を置くことになったのだろう。これが「恋う」が「思う」の類の動詞に分類された、ということである。
      <br>　「君に恋する」だけでは「君を恋しく思う」というニュアンスは含まれず、「君に恋という感情を抱いていますよ」程度のことなのだ。峻別が難しい話ではあるが、これは先述した「恋は気持ちのみを表し、思いは表さない」という話と繋がってくる。
      <br>　現代、「恋する」を「恋う」のように「を」を取って使う用法（「故郷を恋する」など）も辞書に掲載されていて、その場合はやはり「を」を取るのだが、実際にはどう使われているのだろうか。
      <br>「現代日本語書き言葉均衡コーパス『少納言』」を用いて検索した。これは書籍や新聞、国会会議録までの多数のデータを検索対象としているもので、ある文字列に対してどれほどの用例があるのか調べることができる。
      <br>　検索した結果、「恋する」が一九一件、「（を）恋する」が八〇件、「（に）恋する」が一〇四件となった（「を」「に」は前文脈指定。直前でなくても前の文脈に含まれていれば認識される）。ちなみに、前文脈指定をしない、直接の「を恋する」は八件。「に恋する」は一八件であった。
      <br>　検索結果からすると、僅差で「に」が優勢とは言え、「を」恋するというのも決して無視できない数であると分かった。
      <br>　つまり、現代語としては「君を恋する」という言い回しは決しておかしくないどころか、辞書にも収録され、人口に膾炙している表現だと言える。
    </p>

    <div class="ft-subtitle">五　「恋をする」</div>
    <p class="ft-text">
      <br>　但し、一つ注意すべきことがある。それは、「君を恋する」は、「君を恋をする」とは訳せない、ということだ。「恋う」のところで説明したように、「を」を取るということは一つの動詞としてまとまりが強くある必要があるのだ。
      <br>　「君に恋する」が「君に恋をする」と訳せることからも（第三章を参照されたい）、「に」を取るときは「恋＋する」という形が少なからず意識されていると言える。一方で「を」を取るときは「恋する」が一つの動詞として認識されているのだ。そして、特に「を」を取ったときに「恋しく思う」というニュアンスが生まれてくるのだ。
    </p>

    <div class="ft-subtitle">六　辞書的観点</div>
    <p class="ft-text">
      <br>　さて、これまで「恋してる」という言葉が二人称に用いうるか、という問題について辞書的な観点から切り込んできた。
      <br>　「君に恋する」と言ってしまえばどこか第三者的な言い方に聞こえるが、「君を恋する」との言い回しで「君を恋しく思っています」という意味を表せるようだから、二人称で「恋する」を使うことも正しいようだ。
      <br>　では、なぜ相手に「恋してる」という言葉を「愛してる」のように使えないのか、最後にニュアンスの面を考えていこう。    </p>
    <div class="ft-subtitle">七　ニュアンスの差</div>
    <p class="ft-text">
      <br>　「恋」という言葉のニュアンスを摑むべく、長らく動詞として用いられてきた「恋う」に注目してみよう。
      <br>　広辞苑の「恋う」の語釈からもわかる通り、「恋う」はそもそも「故郷を恋う」「亡き母を恋う」のように『今、手元にないものを求める』というようなニュアンスが存在する。「恋慕」なんて言葉もあったり恋の語釈に恋い慕うと書いてあったりするが、そもそも「慕う」の語釈を見ても『恋しく思い、また離れがたく思ってあとを追って行く』とあるではないか。
      <br>　つまり、「恋」は、『ないものを求める』、または「慕う」の通りに『なくならないように縋ってゆく』といったニュアンスが根底に流れているのだ。ここが本質である。
      <br>
      <br>　最初の問題提起が少し曖昧だったために混乱してしまうが、相手に面と向かって「君に恋してる」とは言わない。これは、七章で触れた通りに「恋」が「ないものを求める」というニュアンスを持っているからである。それは「恋う」「恋する」といった動詞でも同じことだ。眼前の人物に対して「恋してる」というのはニュアンスからして違和感を覚える。
      <br>　「二人称」で用いるという言葉を広い意味で解釈すれば、「恋する」は、①手紙文中、②独り言において使用可能だろう。
      <br>　①では暫く離れている相手などに「恋しく思います」との意味で「君を恋しています」というふうに使えるだろう。これは五章で触れた通りの「を」を取る「恋する」で、「思う」の意味まで含まれるから二人称として使えるのだ。
      <br>　②は少し特殊な例だ。「に」を取る方の「恋する」、つまり「恋という動作をしています」との意味で「君に恋してる」ということはできる。但し、先程も触れたように「恋」はないものを示しているのに加え、自分が恋という動作をしていることに気づく、といった内省的な言い回しのため、直接言うことは少ないということで、恐らく独り言などにおいてのみ使いうるものだろう。
      <br>
      <br>　まとめると、手紙文中や独り言において、二人称に「恋してる」という言葉を使うことはできる。しかし、面と向かって「恋してる」と言うのは「君を恋してる」でも「君に恋してる」の何れにしても変な文章になってしまうということだ。
    </p>
    <p class="ft-text">
      <br>　以上、「恋する」を色々な視点から見てきたが、どうだっただろうか。一つの言葉に対してこれだけ深く、様々な辞書を取っ替え引っ替え見比べて考えるのは面白いことである。そして、また辞書だけが言葉の意味を表すのではない、ということがおわかり頂けただろうか。
      <br>　皆さんも、日常生活の中でふと気になる言葉があったときに、まずは辞書を引いてみてほしい。きっと言葉の深い世界を知ることができるはずだ。
    </p>
